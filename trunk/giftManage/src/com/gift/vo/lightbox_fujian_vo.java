package com.gift.vo;

public class lightbox_fujian_vo {

	private String fj_name;// 附件名称
	private String fj_path;// 附件地址
	private String fj_type;// 附件类型
	private String fj_desc;// 附件说明
	
	public String getFj_name() {
		return fj_name;
	}
	
	public void setFj_name(String fj_name) {
		this.fj_name = fj_name;
	}
	
	public String getFj_path() {
		return fj_path;
	}
	
	public void setFj_path(String fj_path) {
		this.fj_path = fj_path;
	}
	
	public String getFj_type() {
		return fj_type;
	}
	
	public void setFj_type(String fj_type) {
		this.fj_type = fj_type;
	}
	
	public String getFj_desc() {
		return fj_desc;
	}
	
	public void setFj_desc(String fj_desc) {
		this.fj_desc = fj_desc;
	}

}